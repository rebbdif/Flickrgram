#import "UIFont+SLVFonts.h"

@implementation UIFont (SLVFonts)

+ (UIFont *)sanFranciscoDisplayMedium14 {
    UIFont *font = [UIFont systemFontOfSize:14];
    return font;
}

+ (UIFont *)sanFranciscoDisplayMedium13 {
    return [UIFont systemFontOfSize:13];
}

@end
